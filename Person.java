package personinfo;

public class Person {
	public String name=" ";
	public String address=" ";
	public Person(String name,String address) {
		this.name=name;
		this.address=address;
	}
	public void setName(String name) {
		this.name=name;
	}
	public String getName() {
		return name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	

	
	
}
